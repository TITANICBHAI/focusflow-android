function getDeviceLanguage(): SupportedLanguage {
  try {
    const locales = getLocales();
    if (locales && locales.length > 0) {
      const tag = locales[0].languageTag.toLowerCase();
      if (tag.startsWith('zh')) return 'zh';
      if (tag.startsWith('fr')) return 'fr';
      if (tag.startsWith('de')) return 'de';
      if (tag.startsWith('ja')) return 'ja';
      if (tag.startsWith('ko')) return 'ko';
      if (tag.startsWith('es')) return 'es';
    }
  } catch {}
  return 'en';
}

export function initI18n(storedLanguage?: string | null) {
  const lng: SupportedLanguage =
    (storedLanguage as SupportedLanguage) ?? getDeviceLanguage();

  if (!i18n.isInitialized) {
    void i18n.use(initReactI18next).init({
      resources,
      lng,
      fallbackLng: 'en',
      interpolation: { escapeValue: false },
      compatibilityJSON: 'v4',
    });
  } else {
    void i18n.changeLanguage(lng);
  }

  return i18n;
}

export function changeLanguage(code: SupportedLanguage) {
  return i18n.changeLanguage(code);
}

export default i18n;
